import { format } from 'date-fns';

/**
 * Markdown Generator Utilities
 *
 * This module provides template functions for generating structured markdown
 * documents from threads, reports, and other entities. The output is:
 * - Human-readable and well-formatted
 * - AI-parseable with clear structure
 * - SEO-friendly with proper headings and metadata
 */

export interface ThreadData {
  _id: string;
  title: string;
  description?: string;
  status: string;
  createdAt: Date;
  updatedAt: Date;
  userId: string;
}

export interface MessageData {
  _id: string;
  role: 'user' | 'assistant' | 'system';
  content: string;
  createdAt: Date;
  metadata?: {
    model?: string;
    tokens?: number;
  };
}

export interface ReportData {
  _id: string;
  title?: string;
  htmlContent: string;
  createdAt: Date;
  isInteractiveMode?: boolean;
  metadata?: {
    model?: string;
    tokens?: number;
  };
}

export interface SectionData {
  _id: string;
  title: string;
  htmlContent: string;
  order: number;
  type: string;
  createdAt: Date;
}

/**
 * Generate slug from title
 */
export function generateSlug(title: string, id: string): string {
  const slug = title
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

  // Add first 8 chars of ID for uniqueness
  return `${slug}-${id.substring(0, 8)}`;
}

/**
 * Format date for markdown
 */
export function formatDate(date: Date): string {
  return format(new Date(date), 'MMMM d, yyyy \'at\' h:mm a');
}

/**
 * Convert HTML to markdown-friendly text
 * This is a simple converter - for production, consider using a library like turndown
 */
export function htmlToMarkdown(html: string): string {
  return html
    // Remove script and style tags
    .replace(/<script\b[^<]*(?:(?!<\/script>)<[^<]*)*<\/script>/gi, '')
    .replace(/<style\b[^<]*(?:(?!<\/style>)<[^<]*)*<\/style>/gi, '')
    // Convert headings
    .replace(/<h1[^>]*>(.*?)<\/h1>/gi, '# $1\n\n')
    .replace(/<h2[^>]*>(.*?)<\/h2>/gi, '## $1\n\n')
    .replace(/<h3[^>]*>(.*?)<\/h3>/gi, '### $1\n\n')
    .replace(/<h4[^>]*>(.*?)<\/h4>/gi, '#### $1\n\n')
    // Convert paragraphs
    .replace(/<p[^>]*>(.*?)<\/p>/gi, '$1\n\n')
    // Convert lists
    .replace(/<li[^>]*>(.*?)<\/li>/gi, '- $1\n')
    .replace(/<ul[^>]*>(.*?)<\/ul>/gi, '$1\n')
    .replace(/<ol[^>]*>(.*?)<\/ol>/gi, '$1\n')
    // Convert links
    .replace(/<a[^>]*href="([^"]*)"[^>]*>(.*?)<\/a>/gi, '[$2]($1)')
    // Convert bold and italic
    .replace(/<strong[^>]*>(.*?)<\/strong>/gi, '**$1**')
    .replace(/<b[^>]*>(.*?)<\/b>/gi, '**$1**')
    .replace(/<em[^>]*>(.*?)<\/em>/gi, '*$1*')
    .replace(/<i[^>]*>(.*?)<\/i>/gi, '*$1*')
    // Convert line breaks
    .replace(/<br\s*\/?>/gi, '\n')
    // Remove remaining HTML tags
    .replace(/<[^>]+>/g, '')
    // Decode HTML entities
    .replace(/&nbsp;/g, ' ')
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    // Clean up extra whitespace
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

